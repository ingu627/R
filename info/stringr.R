# stringr 패키지

# str_detect : 해당 단어가 포함된 문자열에 대해 T/F 반환
# str_count : 주어진 단어에서 해당 글자가 몇 번 나오는 지 알려줌
# str_c : 문자열을 빈 칸 없이 이어붙임
# str_dup : 주어진 문자열을 주어진 횟수만큼 반복해서 이어 붙임
# str_length : 주어진 문자열의 길이 출력 (몇 글자인지)
# str_locate : 주어진 문자열에서 특정문자가 처음 및 끝에 나오는 위치 반환
# str_replace : 변경 전 문자를 변경 후 문자로 바꿈.
# str_split : 문자열을 기준에 대해 분리
# str_sub : 주어진 문자열에서 지정된 길이만큼 문자를 잘라냄
# str_trim : 문자열의 앞과 뒤에 공백이 있을 경우 제거
# str_extract : 단어에서 pattern만 뽑아서 pattern에 일치하는 값만 반환
